  late final String id;
  String name;
  String nation;
  String region;
  String city;
  String address;
  double latitude;
  double longitude;

  Location({
    required this.name,
    required this.address,
    required this.nation,
    required this.region,
    required this.city,
    required this.latitude,
    required this.longitude,
  });



}